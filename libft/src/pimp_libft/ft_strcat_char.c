#include "../../include/pimp_libft/my_libft.h"

char	*ft_strcat_char(char *str, char c)
{
	int		i;

	i = -1;
	while(str[++i])
		;
	str[i] = c;
	str[i + 1] = '\0';
	return(str);
}
